	<div class="box">
		<h3>Our Impact</h3>
		<p>Within government, we build a low-risk setting for innovation by encouraging departments like Boston’s <a href="http://www.cityofboston.gov/newurbanmechanics/ ">Urban Mechanics Office</a>, positions like Philadelphia’s <a href="http://technical.ly/philly/2011/08/11/adel-ebeid-meet-the-new-cto-of-the-city-of-philadelphia/">Chief Innovation Officer</a> and open policies, datasets and practices. In the community, we engage citizens in building <a href="http://codeforamerica.org/apps">apps</a>, <a href="http://brigade.codeforamerica.org ">knowledge hubs</a> and <a href="http://codeforamerica.org/2012/07/12/a-city-partnership-extraordinaire/ ">local workshops</a> with the intent to scale globally. And in the civic tech marketplace, we support <a href="http://codeforamerica.org/accelerator ">civic startups</a>, <a href="http://codeforamerica.org/fellows/">public/private funding collaborations</a> and <a href="http://codeforamerica.org/2013/05/07/towards-a-procurement-strategy/">open procurement</a>.</p>
