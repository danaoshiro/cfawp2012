	<h3>what we do</h3>
		<p>Through the <a href="http://codeforamerica.org/fellows">Fellowship</a>, <a href="http://brigade.codeforamerica.org">Brigade</a>, <a href="http://codeforamerica.org/accelerator/">Accelerator</a>, <a href="http://peernetwork.in/">Peer Network</a> and <a href="http://codeforamerica.org/international/">Code for All</a>, Code for America aims to fix the relationships between citizens and government while simultaneously changing the culture of government itself.  We help governments restructure to create low-risk settings for innovation, engage citizens in creating better apps and services and support ongoing competition in the government tech marketplace.</p>
		<p align="right"><a href="/programs" class="more">Our Programs</a></p> 
